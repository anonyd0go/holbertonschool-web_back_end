#!/usr/bin/env python3
"""
This module provides a function to compute the floor of a float number.

Functions:
    floor(n: float) -> int:
        Returns the floor of a floating-point number as an integer.
"""


def floor(n: float) -> int:
    """
    Compute the floor of a floating-point number.

    Args:
        n (float): The floating-point number to floor.

    Returns:
        int: The largest integer less than or equal to the given number.
    """
    return int(n)
